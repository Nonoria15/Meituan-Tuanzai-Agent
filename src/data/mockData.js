function friendText(selectedFriends = []) {
  return selectedFriends
    .map((friend) => [friend.id, friend.name, friend.relation, friend.summary, ...(friend.tags || [])].join(' '))
    .join(' ');
}

export function inferSceneType(request = '', selectedFriends = []) {
  const friendInfo = friendText(selectedFriends);
  const text = `${request} ${friendInfo}`;
  const requestLooksElderly = /妈妈|老人|家人|不要太晚|好停车|安静餐厅|带.*少走路|少走路.*安静/.test(request);
  const friendLooksElderly = selectedFriends.some((friend) => {
    const info = [friend.name, friend.relation, ...(friend.tags || [])].join(' ');
    return /妈妈|爸爸|老人|家人|孩子|宝宝|亲子|不要太晚/.test(info);
  });
  const hasFamily = requestLooksElderly || friendLooksElderly;
  const hasSocial = /朋友|聚会|小雨|阿哲|Leo|Livehouse|火锅|烧烤|拍照|日料|约会/.test(text);
  if (selectedFriends.length > 1 && hasFamily && hasSocial) return 'mixed';
  if (friendLooksElderly || requestLooksElderly) return 'elderly';
  const matched = sceneKeywords.find(([, pattern]) => pattern.test(text));
  return matched?.[0] || 'friends';
}

export function generatePlans(request = '', selectedFriends = []) {
  const sceneType = inferSceneType(request, selectedFriends);
  const source = scenePlans[sceneType] || plans;
  return source.map((plan, index) => ({
    ...plan,
    id: ['A', 'B', 'C'][index] || plan.id,
    sceneType,
  }));
}

